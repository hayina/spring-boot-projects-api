package api.beans;

public class ParentChildBean {
	
	
	public Integer id;
	public String label;
	public Integer idParent;
	public String labelParent;
	
	
	public ParentChildBean() {}
	
	
	public ParentChildBean(Integer id, String label, Integer idParent, String labelParent) {
		super();
		this.id = id;
		this.label = label;
		this.idParent = idParent;
		this.labelParent = labelParent;
	}
	
	
	
	

}
